function drawPlayer() {
    canvasContext.fillStyle = PLAYER.color;
    canvasContext.beginPath();
    canvasContext.arc(PLAYER.x, PLAYER.y, PLAYER.r, 0, 2 * Math.PI);
    canvasContext.closePath();
    canvasContext.fill();

    if (GAME.round === 1) {
        lab(ROUND1);
    }
    if (GAME.round === 2) {
        lab(ROUND2);
    }
    if (GAME.round === 3) {
        lab(ROUND3);
    }
    if (GAME.round === 4) {
        lab(ROUND4);
    }
}
