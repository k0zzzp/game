canvasContext.fillRect(0, 0, GAME.width, GAME.height);

var STARTPUSH = {
    x: 200,
    y: 450,
    width: 200,
    height: 100,
    color: 'grey',
    background: "black"
}


var MESSAGE = {
    text: "START",
    x: 235,
    y: 515,
    color: "white"
}

var LEGEND_MESSAGE = {
    text: "Click START to continue",
    x: 75,
    y: 250,
    color: "red"
}

function startScreenPlay() {
    drawStartScreen();
    if (GAME.isstarted === 0) {
        requestAnimationFrame(startScreenPlay);
    }

}

function drawStartScreen() {
    drawButton();
    drawMessage();
}

function drawButton() {
    canvasContext.fillStyle = STARTPUSH.color;
    canvasContext.fillRect(STARTPUSH.x, STARTPUSH.y, STARTPUSH.width, STARTPUSH.height);
}


function drawMessage() {
    canvasContext.font = "40px Arial";
    canvasContext.fillStyle = MESSAGE.color;
    canvasContext.fillText(MESSAGE.text, MESSAGE.x, MESSAGE.y);

    canvasContext.font = "40px Arial";
    canvasContext.fillStyle = LEGEND_MESSAGE.color;
    canvasContext.fillText(LEGEND_MESSAGE.text, LEGEND_MESSAGE.x, LEGEND_MESSAGE.y);
}

function drawPlayer() {
    canvasContext.fillStyle = PLAYER.color;
    canvasContext.beginPath();
    canvasContext.arc(PLAYER.x, PLAYER.y, PLAYER.r, 0, 2 * Math.PI);
    canvasContext.closePath();
    canvasContext.fill();

    if (GAME.round === 1) {
        lab(ROUND1);
    }
    if (GAME.round === 2) {
        lab(ROUND2);
    }
    if (GAME.round === 3) {
        lab(ROUND3);
    }
    if (GAME.round === 4) {
        lab(ROUND4);
    }
}

function lab(round0) {
    var indx = Math.floor((PLAYER.x) / 15), indy = Math.floor((PLAYER.y + PLAYER.r) / 15);
    if (round0[indy][indx] === 0) {
        GAME.isfinished = 1;
    }
    if (round0[indy][indx] === 2) {
        if (GAME.round != 4) {
            GAME.iswin = 1;
        }
        else {
            GAME.isfinished = 1;
        }

    }
}

function drawBackground() {
    canvasContext.fillStyle = GAME.background;
    canvasContext.fillRect(0, 0, GAME.width, GAME.height);
}

function drawFrame() {
    canvasContext.clearRect(0, 0, GAME.width, GAME.height);
    drawBackground();
    if (GAME.round === 1) {
        drawRound(ROUND1);
    }
    if (GAME.round === 2) {
        drawRound(ROUND2);
    }
    if (GAME.round === 3) {
        drawRound(ROUND3)
    }
    if (GAME.round === 4) {
        drawRound(ROUND4);
    }
    drawPlayer();
}

function drawRound(round0) {
    var a = 0;
    for (let i = 0; i < 40; i++) {
        for (let j = 0; j < 40; j++) {
            a = round0[j][i];
            canvasContext.fillStyle = ROUND[a];
            canvasContext.fillRect(15 * i, 15 * j, 15, 15);
        }
    }
}


function play() {
    drawFrame();
    if (GAME.iswin) {
        GAME.iswin = 0;
        GAME.round += 1;
    }
    if (GAME.isfinished === 0) {
        requestAnimationFrame(play);
    }
    if (GAME.isfinished) {
        var AUDIO = {
            src: new Audio('game\audio\audioscream.mp3'),
            audioIsOn: true,
        }

        if (GAME.round === 4) {
            var img = new Image();
            img.src = "скример.jpg";
            AUDIO.autoplay = true;
            img.onload = () => canvasContext.drawImage(img, 0, 0, 600, 600);
        }
        else {
            canvasContext.fillStyle = GAME.background;
            canvasContext.fillRect(0, 0, GAME.width, GAME.height);
            canvasContext.font = "40px Arial";
            canvasContext.fillStyle = "white";
            canvasContext.fillText("GAME IS OVER", 150, 300);
        }


    }
}
function initEventListeners() {
    window.addEventListener("mousemove", onCanvasMouseMove);
    window.addEventListener('keydown', onCanvasKeyDown);
    window.addEventListener("click", startGame);
}

function onCanvasMouseMove(event) {
    PLAYER.x = event.clientX;
    PLAYER.y = event.clientY;
}
function onCanvasKeyDown(event) {
    if (event.key === "p") {
        Audio.audioIsOn = !Audio.audioIsOn
    }
}
function isOnStartButton(event) {
    if (((event.x >= STARTPUSH.x) && (event.x <= STARTPUSH.x + STARTPUSH.width)) &&
        ((event.y >= STARTPUSH.y) && (event.y <= STARTPUSH.y + STARTPUSH.height))) {
        return true;
    }
}
function startGame(event) {
    if (isOnStartButton(event)) {
        GAME.isstarted = 1;
        canvasContext.clearRect(0, 0, GAME.width, GAME.height);
        play();
    }
}

initEventListeners();
startScreenPlay();
