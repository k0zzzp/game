function lab(round0) {
    var indx = Math.floor((PLAYER.x) / 15), indy = Math.floor((PLAYER.y + PLAYER.r) / 15);
    if (round0[indy][indx] === 0) {
        GAME.isfinished = 1;
    }
    if (round0[indy][indx] === 2) {
        if (GAME.round != 4) {
            GAME.iswin = 1;
            if (AUDIO_NEXT.audioIsOn) {
                AUDIO_NEXT.src.play()
            }
        }
        else {
            GAME.isfinished = 1;
        }

    }
}

function drawBackground() {
    canvasContext.fillStyle = GAME.background;
    canvasContext.fillRect(0, 0, GAME.width, GAME.height);
}

function drawFrame() {
    canvasContext.clearRect(0, 0, GAME.width, GAME.height);
    drawBackground();
    if (GAME.round === 1) {
        drawRound(ROUND1);
        drawTextRound(TEXT_ROUND1);
    }
    if (GAME.round === 2) {
        drawRound(ROUND2);
        drawTextRound(TEXT_ROUND2);
    }
    if (GAME.round === 3) {
        drawRound(ROUND3);
        drawTextRound(TEXT_ROUND3);
    }
    if (GAME.round === 4) {
        drawRound(ROUND4);
        drawTextRound(TEXT_ROUND4);
    }
    drawPlayer();
}

function drawRound(round0) {
    var a = 0;
    for (let i = 0; i < 40; i++) {
        for (let j = 0; j < 40; j++) {
            a = round0[j][i];
            canvasContext.fillStyle = ROUND[a];
            canvasContext.fillRect(15 * i, 15 * j, 15, 15);
        }
    }

}


function play() {
    drawFrame();
    if (GAME.iswin) {
        GAME.iswin = 0;
        GAME.round += 1;
    }
    if (GAME.isfinished === 0) {
        requestAnimationFrame(play);
    }
    if (GAME.isfinished) {

        if (GAME.round === 4) {
            var img = new Image();
            img.src = "скример.jpg";
            if (AUDIO.audioIsOn) {
                AUDIO.src.play()
            }

            AUDIO_BACKGROUND.src.pause()

            img.onload = () => canvasContext.drawImage(img, 0, 0, 600, 600);
        }
        else {
            canvasContext.fillStyle = GAME.background;
            canvasContext.fillRect(0, 0, GAME.width, GAME.height);
            canvasContext.font = "40px Arial Black";
            canvasContext.fillStyle = "white";
            canvasContext.fillText("GAME IS OVER", 140, 300);
            AUDIO_BACKGROUND.src.pause();
            GAME.isstarted = 0;

            restartScreenPlay();

        }


    }
}
